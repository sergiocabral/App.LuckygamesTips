namespace Skript.Core {
    
    /**
     * Tipo para dimensão.
     */
    export type Size = {

        /**
         * Largura.
         * @type {number}
         */
        width: number,

        /**
         * Altura.
         * @type {number}
         */
        height: number
    }

    /**
     * Posicionamento
     */
    export type Position = { 

        /**
         * Posição horizontal.
         * @type {number}
         */
        x: number, 

        /**
         * Posição vertical.
         * @type {number}
         */
        y: number
    }

}
